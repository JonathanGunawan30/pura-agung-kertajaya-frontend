"use client"

import { useEffect } from "react"
import AOS from "aos"
import "aos/dist/aos.css"

export function AOSProvider({ children }: { children: React.ReactNode }) {
    useEffect(() => {
        AOS.init({
            duration: 800,
            once: true,
            easing: 'ease-out-cubic',
            offset: 50,
            delay: 50,
            mirror: false,
            anchorPlacement: 'top-bottom',
        })
    }, [])

    return <>{children}</>
}

